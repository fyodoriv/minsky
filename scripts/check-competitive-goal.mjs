/**
 * One task block parsed out of TASKS.md.
 *
 * @typedef {object} TaskBlock
 * @property {string} id
 * @property {string} priority           — "P0" | "P1" | "P2" | "P3"
 * @property {number} startLine          — 1-based
 * @property {boolean} hasCompetitiveGoal
 */

/**
 * Parse TASKS.md into priority-classified task blocks. Lightweight
 * parser — assumes the tasks.md spec shape:
 *   `## P<n>` headings define priority sections;
 *   `- [ ] ...` lines begin a task block;
 *   `**ID**: <id>` line gives the id.
 *
 * @param {string} body
 * @returns {TaskBlock[]}
 */
export function parseTasks(body) {
  const lines = body.split("\n");
  /** @type {TaskBlock[]} */
  const tasks = [];
  let priority = "";
  /** @type {{ startLine: number, idLines: string[], bodyLines: string[] } | null} */
  let current = null;

  const flush = () => {
    if (current === null) return;
    // Orphan tasks (started before any priority heading) are dropped.
    // We MUST clear `current` either way, otherwise the next flush
    // wrongly attributes the orphan to a later priority section.
    if (priority === "") {
      current = null;
      return;
    }
    const idLine = current.idLines.find((l) => /\*\*ID\*\*:/.test(l));
    if (!idLine) {
      current = null;
      return;
    }
    const match = /\*\*ID\*\*:\s*(.+)$/.exec(idLine.trim());
    if (match === null || match[1] === undefined) {
      current = null;
      return;
    }
    const id = match[1].trim();
    const hasCompetitiveGoal = current.bodyLines.some((l) =>
      /^\s*-?\s*\*\*Competitive-goal\*\*:\s*.+\S/.test(l),
    );
    tasks.push({ id, priority, startLine: current.startLine, hasCompetitiveGoal });
    current = null;
  };

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (line === undefined) continue;
    const headingMatch = /^## (P[0-3])\b/.exec(line);
    if (headingMatch !== null && headingMatch[1] !== undefined) {
      flush();
      priority = headingMatch[1];
      continue;
    }
    if (/^- \[[ x]\] /.test(line)) {
      flush();
      current = { startLine: i + 1, idLines: [], bodyLines: [] };
      continue;
    }
    if (current !== null) {
      current.idLines.push(line);
      current.bodyLines.push(line);
    }
  }
  flush();
  return tasks;
}

/**
 * Decide which task blocks violate the rule.
 *
 * @param {TaskBlock[]} tasks
 * @returns {{ violators: TaskBlock[], grandfathered: TaskBlock[] }}
 */
export function classifyTasks(tasks) {
  /** @type {TaskBlock[]} */
  const violators = [];
  /** @type {TaskBlock[]} */
  const grandfathered = [];
  for (const t of tasks) {
    if (t.priority !== "P0" && t.priority !== "P1") continue;
    if (t.hasCompetitiveGoal) continue;
    if (COMPETITIVE_GOAL_ALLOWLIST.has(t.id)) {
      grandfathered.push(t);
    } else {
      violators.push(t);
    }
  }
  return { violators, grandfathered };
}

function main() {
  const tasksPath = resolve(REPO_ROOT, "TASKS.md");
  const body = readFileSync(tasksPath, "utf8");
  const tasks = parseTasks(body);
  const { violators, grandfathered } = classifyTasks(tasks);

  const total = tasks.filter((t) => t.priority === "P0" || t.priority === "P1").length;
  const clean = total - violators.length - grandfathered.length;

  if (violators.length > 0) {
    process.stderr.write(
      `check-competitive-goal: ${violators.length} P0/P1 task(s) lack the \`**Competitive-goal**:\` field.\n\n`,
    );
    for (const v of violators) {
      process.stderr.write(`  ${v.priority} ${v.id} (TASKS.md:${v.startLine})\n`);
    }
    process.stderr.write(
      "\nAdd a single-line `**Competitive-goal**:` field naming which scorecard metric the task moves.\n" +
        "Example:\n" +
        "  - **Competitive-goal**: drives `autonomous-merge-rate` toward 0.80 — closes gap vs Cursor (0.804) and Claude Code (0.726).\n" +
        "\nSee `vision.md` § 8 (Pattern conformance index, row for `@minsky/competitive-benchmark`) and\n" +
        "`docs/plans/self-metrics-competitive-benchmark.md` slice (d) for context.\n",
    );
    process.exit(1);
  }

  process.stdout.write(
    `check-competitive-goal ok: scanned ${total} P0/P1 task block(s); clean=${clean}, grandfathered=${grandfathered.length}, violators=0\n`,
  );
  if (grandfathered.length > 0) {
    process.stdout.write(
      `  ${grandfathered.length} grandfathered task(s) (will block when modified or once backfilled)\n`,
    );
  }
}

if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}
