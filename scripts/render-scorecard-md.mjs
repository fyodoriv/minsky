#!/usr/bin/env node
// @ts-check
// <!-- scope: human-approved sub-task `path-a-phase-10-render-scorecard-static-md`. One-shot renderer for `competitors/scorecard.md` from scripts/benchmark-run.mjs. -->
//
// render-scorecard-md — invokes scripts/benchmark-run.mjs --json
// directly (bypasses the deleted `bin/minsky competitive` CLI shim,
// Path-A phase-10 sub-task 2) and renders the result as a static
// markdown table at `competitors/scorecard.md`. Re-runnable per the
// corpus-freshness 90-day cadence; the file is the durable readable
// artifact that survives the upcoming `novel/competitive-benchmark/`
// renderer-substrate deletion (sub-task 3).
//
// Anchors: rule #1 (delegate → contribute → absorb); Path A § Phase 10;
// Jimenez et al. *SWE-bench* ICLR 2024 (published-corpus pattern).

import { execSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const REPO_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");

/** @returns {void} */
function main() {
  const raw = execSync("node scripts/benchmark-run.mjs --json", {
    cwd: REPO_ROOT,
    encoding: "utf8",
  });
  /** @type {ScorecardJson} */
  const data = JSON.parse(raw);
  const markdown = renderScorecardMd(data);
  const outPath = resolve(REPO_ROOT, "competitors/scorecard.md");
  writeFileSync(outPath, markdown);
  process.stdout.write(`wrote ${outPath} (${markdown.split("\n").length} lines)\n`);
}

/**
 * @typedef {object} Cell
 * @property {string} metricId
 * @property {string} competitorId
 * @property {number | null} minskyValue
 * @property {number | undefined} [competitorValue]
 * @property {number | undefined} [delta]
 */

/**
 * @typedef {object} Metric
 * @property {string} id
 * @property {string} label
 */

/**
 * @typedef {object} Competitor
 * @property {string} id
 * @property {string} label
 * @property {string} kind
 */

/**
 * @typedef {object} CorpusFreshnessEntry
 * @property {string} id
 * @property {string} asOf
 * @property {number} ageDays
 * @property {string} status
 */

/**
 * @typedef {object} ScorecardJson
 * @property {string} generatedAt
 * @property {number} cellCount
 * @property {number} comparisonCount
 * @property {Cell[]} cells
 * @property {Metric[]} metrics
 * @property {Competitor[]} competitors
 * @property {{ meetsM110: boolean; liveDeltaCount: number; competitorsWithData: number; metricsWithComparison: number; gap: string }} acceptance
 * @property {{ generatedAt: string; entries: CorpusFreshnessEntry[] }} corpusFreshness
 */

/**
 * Pure renderer — takes parsed JSON, returns markdown.
 *
 * @param {ScorecardJson} data
 * @returns {string}
 */
export function renderScorecardMd(data) {
  const lines = [];
  lines.push("# Competitive scorecard");
  lines.push("");
  lines.push(`<!-- generated by scripts/render-scorecard-md.mjs at ${data.generatedAt} -->`);
  lines.push("<!-- regenerate with: node scripts/render-scorecard-md.mjs -->");
  lines.push("");
  lines.push("Static snapshot of `bin/minsky competitive --json`. This file replaces");
  lines.push(
    "the executable CLI surface per Path-A § Phase 10 (`docs/plans/2026-05-24-path-a-aggressive-cut.md`).",
  );
  lines.push("");
  lines.push(`- **Cells**: ${String(data.cellCount)}`);
  lines.push(`- **Comparisons (live delta)**: ${String(data.comparisonCount)}`);
  lines.push(`- **M1.10 acceptance**: ${data.acceptance.meetsM110 ? "met" : "NOT met"}`);
  lines.push(`- **Competitors with data**: ${String(data.acceptance.competitorsWithData)}`);
  lines.push(`- **Metrics with comparison**: ${String(data.acceptance.metricsWithComparison)}`);
  if (data.acceptance.gap) {
    lines.push(`- **Gap**: ${data.acceptance.gap}`);
  }
  lines.push("");

  // Section: comparisons that actually have data.
  lines.push("## Live comparisons (cells with `competitorValue` and/or `minskyValue`)");
  lines.push("");
  lines.push("| Metric | Competitor | Minsky | Competitor | Delta |");
  lines.push("|---|---|---|---|---|");
  const nonNullCells = data.cells.filter(
    (c) =>
      c.minskyValue !== null ||
      typeof c.competitorValue === "number" ||
      typeof c.delta === "number",
  );
  for (const cell of nonNullCells) {
    const metric = data.metrics.find((m) => m.id === cell.metricId)?.label ?? cell.metricId;
    const competitor =
      data.competitors.find((c) => c.id === cell.competitorId)?.label ?? cell.competitorId;
    const minsky = formatCellValue(cell.minskyValue);
    const compVal = formatCellValue(cell.competitorValue);
    const delta = formatCellValue(cell.delta);
    lines.push(`| ${metric} | ${competitor} | ${minsky} | ${compVal} | ${delta} |`);
  }
  lines.push("");

  // Section: corpus freshness.
  lines.push("## Corpus freshness");
  lines.push("");
  lines.push(`Generated at ${data.corpusFreshness.generatedAt}.`);
  lines.push("");
  lines.push("| Competitor | As of | Age (days) | Status |");
  lines.push("|---|---|---|---|");
  for (const entry of data.corpusFreshness.entries) {
    const label = data.competitors.find((c) => c.id === entry.id)?.label ?? entry.id;
    lines.push(`| ${label} | ${entry.asOf} | ${String(entry.ageDays)} | ${entry.status} |`);
  }
  lines.push("");

  // Section: all metrics catalogue (durable reference).
  lines.push("## Metrics catalogue");
  lines.push("");
  lines.push("| Id | Label |");
  lines.push("|---|---|");
  for (const metric of data.metrics) {
    lines.push(`| \`${metric.id}\` | ${metric.label} |`);
  }
  lines.push("");

  // Section: competitors catalogue.
  lines.push("## Competitors catalogue");
  lines.push("");
  lines.push("| Id | Label | Kind |");
  lines.push("|---|---|---|");
  for (const competitor of data.competitors) {
    lines.push(`| \`${competitor.id}\` | ${competitor.label} | ${competitor.kind} |`);
  }
  lines.push("");

  return lines.join("\n");
}

/**
 * @param {number | null | undefined} value
 * @returns {string}
 */
function formatCellValue(value) {
  if (value === null || value === undefined) return "—";
  if (Number.isInteger(value)) return String(value);
  return value.toFixed(3);
}

if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}
