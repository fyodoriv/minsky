#!/usr/bin/env node
// <!-- scope: human-approved 2026-06-11 operator task "daily METRICS.md regen lands on main" — heal for the daily metric-freshness red (main went red 2026-06-11T00:00Z, hand-unwedged by PR #1208) -->
//
// daily-metrics-regen — orchestrates the daily docs/METRICS.md refresh so
// the committed metric surface lands on main BEFORE the 1-day freshness
// budget on `cross-repo-pr-rate` expires at midnight UTC.
//
// Pattern: pure decision core (`decideRegen` + builders) over snapshots of
// repo state + a thin CLI that calls the canonical pipeline scripts
// (`collect-metrics.mjs` → `metrics-render.mjs` — rule #1, reuse, don't
// reimplement) and git plumbing. Mirrors `auto-merge-clean-prs.mjs`'s
// shape: tests inject synthetic state; production calls `git`/`gh` via
// execFileSync.
//
// Why plumbing, not checkout: the host repo is SHARED with a live daemon
// (operator-tree wipe incident, PR #1194 guard). This script NEVER touches
// HEAD, the index, or the working tree — it renders to a tempfile,
// hash-objects the blob, builds a tree + commit on top of `origin/main`
// with a temporary GIT_INDEX_FILE, and pushes the commit sha directly to
// `refs/heads/chore/metrics-daily-regen-<date>`.
//
// Idempotence (one PR per UTC date, safe to fire hourly):
//   1. main's docs/METRICS.md already references today's snapshot → exit 0.
//   2. the regen branch already exists on origin → exit 0.
//   3. the fresh render is byte-identical to main's copy → exit 0.
//
// launchd-safe: node-script children are spawned via `process.execPath`
// (never bare `node`); `gh` resolution comes from the runner
// (`distribution/systemd/run-daily-metrics.sh`) sourcing
// `lib-launchd-path.sh`.
//
// Source: `chore(metrics): daily regen 2026-06-11 — unwedge metric-freshness
// gate` (#1208) names this script as durable fix (b); experiments/
// daily-metrics-regen-lands-on-main.yaml is the rule-#9 pre-registration.
//
// Pivot (rule #9): see the experiment record — if the scheduled PR path
// produces ≥2 broken/duplicate PRs per week, pivot to direct-push-to-main
// of the regen commit (smaller surface, no PR gates) or to widening the
// freshness budget.

import { execFileSync } from "node:child_process";
import { existsSync, mkdtempSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { join, resolve } from "node:path";
import process from "node:process";

/** Canonical rendered-metrics path, kept aligned with
 * `metrics-render.mjs`'s `DEFAULT_OUTPUT_RELATIVE` (rule #2). */
export const METRICS_RELATIVE = "docs/METRICS.md";

/**
 * @param {string} dateUtc — `YYYY-MM-DD`
 * @returns {string}
 */
export function regenBranchName(dateUtc) {
  return `chore/metrics-daily-regen-${dateUtc}`;
}

/**
 * The renderer stamps the snapshot source path
 * (`.minsky/metric-snapshots/<date>.json`) into the rendered markdown, so
 * the committed file itself records which UTC date it was rendered from.
 *
 * @param {string} metricsMdText — committed docs/METRICS.md content
 * @param {string} dateUtc — `YYYY-MM-DD`
 * @returns {boolean}
 */
export function alreadyRenderedForDate(metricsMdText, dateUtc) {
  return metricsMdText.includes(`.minsky/metric-snapshots/${dateUtc}.json`);
}

/**
 * Conventional commit message; header stays ≤72 chars.
 *
 * @param {string} dateUtc
 * @returns {string}
 */
export function buildCommitMessage(dateUtc) {
  return [
    `chore(metrics): daily regen ${dateUtc} — keep metric-freshness green`,
    "",
    "Scheduled docs/METRICS.md refresh (collect-metrics + metrics-render)",
    "so the cross-repo-pr-rate 1d freshness budget never expires on main.",
    "Opened by scripts/daily-metrics-regen.mjs via the",
    "com.minsky.daily-metrics / minsky-daily-metrics.timer supervisor unit.",
  ].join("\n");
}

/**
 * PR body with the four required blocks: `## Why needed`,
 * `## Hypothesis self-grade` (Predicted/Observed/Match/Lesson — Observed
 * carries the live `check-metric-freshness` output), the security
 * opt-out marker, and `## Vision trace`.
 *
 * @param {{ dateUtc: string, freshnessOutput: string }} args
 * @returns {string}
 */
export function buildPrBody({ dateUtc, freshnessOutput }) {
  const observed = freshnessOutput.trim() === "" ? "(no output captured)" : freshnessOutput.trim();
  return [
    "## Why needed",
    "",
    `docs/METRICS.md is the committed metric surface; the \`cross-repo-pr-rate\``,
    "row carries a 1-day freshness budget enforced by the whole-tree",
    "`check-metric-freshness` gate. Without a daily regen landing on main, the",
    "gate goes red at midnight UTC and blocks every PR (last manual unwedge:",
    `PR #1208). This PR is the scheduled ${dateUtc} refresh, generated by`,
    "`scripts/daily-metrics-regen.mjs` (collect-metrics → metrics-render, the",
    "canonical pipeline) and containing ONLY the rendered docs/METRICS.md.",
    "",
    "## Hypothesis self-grade",
    "",
    "- Predicted: a scheduled collect+render+PR keeps `check-metric-freshness`",
    "  green on main with zero manual unwedges",
    "  (experiments/daily-metrics-regen-lands-on-main.yaml).",
    "- Observed: freshness check against the rendered file at generation time:",
    "",
    "  ```text",
    ...observed.split("\n").map((line) => `  ${line}`.trimEnd()),
    "  ```",
    "",
    "- Match: yes — the rendered surface passes the gate this PR exists to keep",
    "  green.",
    "- Lesson: generated-doc refreshes must land on main on the budget cadence,",
    "  not merely render locally; the daemon-side render alone left main red.",
    "",
    "## Security & privacy",
    "",
    "<!-- security: not-applicable — generated metrics doc refresh -->",
    "",
    "## Vision trace",
    "",
    "- **Vision goal**: vision.md rule #4 — everything measurable, everything",
    "  visible; docs/METRICS.md is the committed audit surface this refresh",
    "  keeps trustworthy.",
    "- **User story**: N/A — generated-doc refresh (scenario lives in",
    "  user-stories/007-agent-self-heals-catalogued-failures.md).",
    "- **Competitor prior art**: N/A — internal scheduled maintenance, no",
    "  competitor surface.",
  ].join("\n");
}

/**
 * @typedef {(
 *   | { action: "skip", reason: "already-rendered-today" | "branch-exists" | "no-change" }
 *   | { action: "publish", branch: string, commitMessage: string }
 * )} RegenDecision
 */

/**
 * Pure decision core: given snapshots of repo state, decide whether this
 * fire publishes a regen PR or exits silently.
 *
 * @param {{
 *   todayUtc: string,
 *   mainMetricsMd: string,
 *   renderedMetricsMd: string | null,
 *   remoteBranchExists: boolean,
 * }} args — `renderedMetricsMd` is null when the pipeline hasn't run yet
 *   (the caller short-circuits on the first two guards before rendering).
 * @returns {RegenDecision}
 */
export function decideRegen({ todayUtc, mainMetricsMd, renderedMetricsMd, remoteBranchExists }) {
  if (alreadyRenderedForDate(mainMetricsMd, todayUtc)) {
    return { action: "skip", reason: "already-rendered-today" };
  }
  if (remoteBranchExists) {
    return { action: "skip", reason: "branch-exists" };
  }
  if (renderedMetricsMd !== null && renderedMetricsMd === mainMetricsMd) {
    return { action: "skip", reason: "no-change" };
  }
  return {
    action: "publish",
    branch: regenBranchName(todayUtc),
    commitMessage: buildCommitMessage(todayUtc),
  };
}

// ---------------------------------------------------------------------------
// CLI binding — the only I/O surface below this line.
// ---------------------------------------------------------------------------

/**
 * @param {string} cmd
 * @param {string[]} args
 * @param {{ cwd: string, env?: Record<string, string | undefined>, allowFail?: boolean }} opts
 * @returns {{ status: number, stdout: string, stderr: string }}
 */
function run(cmd, args, opts) {
  try {
    const stdout = execFileSync(cmd, args, {
      cwd: opts.cwd,
      encoding: "utf8",
      env: /** @type {NodeJS.ProcessEnv} */ ({ ...process.env, ...(opts.env ?? {}) }),
      timeout: 300_000,
      maxBuffer: 16 * 1024 * 1024,
    });
    return { status: 0, stdout, stderr: "" };
  } catch (err) {
    const e =
      /** @type {{ status?: number, stdout?: Buffer | string, stderr?: Buffer | string, message?: string }} */ (
        err
      );
    const result = {
      status: typeof e.status === "number" ? e.status : 1,
      stdout: e.stdout?.toString() ?? "",
      stderr: e.stderr?.toString() ?? e.message ?? "",
    };
    if (opts.allowFail) return result;
    throw new Error(
      `command failed (exit ${result.status}): ${cmd} ${args.join(" ")}\n${result.stderr}`,
    );
  }
}

/** @returns {string} */
function todayUtcDate() {
  return new Date().toISOString().slice(0, 10);
}

/**
 * @param {string} root
 * @returns {number}
 */
function cliMain(root) {
  // 1. Verify this is a minsky host with the canonical pipeline present.
  for (const required of [
    METRICS_RELATIVE,
    "scripts/collect-metrics.mjs",
    "scripts/metrics-render.mjs",
  ]) {
    if (!existsSync(resolve(root, required))) {
      process.stderr.write(`daily-metrics-regen: not a minsky repo — missing ${required}\n`);
      return 2;
    }
  }
  // A dirty docs/METRICS.md in the shared tree means a manual unwedge or a
  // daemon render is in flight — refuse loudly rather than race it.
  const dirty = run("git", ["status", "--porcelain", "--", METRICS_RELATIVE], { cwd: root });
  if (dirty.stdout.trim() !== "") {
    process.stderr.write(
      `daily-metrics-regen: ${METRICS_RELATIVE} has uncommitted changes in the working tree — refusing to race a manual edit\n`,
    );
    return 3;
  }

  // 2. Fresh view of origin/main (the branch base + the committed surface).
  run("git", ["fetch", "--quiet", "origin", "main"], { cwd: root });
  const mainSha = run("git", ["rev-parse", "origin/main"], { cwd: root }).stdout.trim();
  const mainMetricsMd = run("git", ["show", `origin/main:${METRICS_RELATIVE}`], {
    cwd: root,
  }).stdout;

  const today = todayUtcDate();
  const branch = regenBranchName(today);
  const lsRemote = run("git", ["ls-remote", "--heads", "origin", branch], { cwd: root });
  const remoteBranchExists = lsRemote.stdout.trim() !== "";

  const earlyDecision = decideRegen({
    todayUtc: today,
    mainMetricsMd,
    renderedMetricsMd: null,
    remoteBranchExists,
  });
  if (earlyDecision.action === "skip") return 0;

  // 3. Run the canonical pipeline: collect today's snapshot, then render to
  //    a TEMPFILE (never the shared working tree).
  const scratch = mkdtempSync(join(tmpdir(), "daily-metrics-regen-"));
  try {
    run(process.execPath, ["scripts/collect-metrics.mjs"], { cwd: root });
    const renderedPath = join(scratch, "METRICS.md");
    run(process.execPath, ["scripts/metrics-render.mjs", "--output", renderedPath], { cwd: root });
    const renderedMetricsMd = readFileSync(renderedPath, "utf8");

    const decision = decideRegen({
      todayUtc: today,
      mainMetricsMd,
      renderedMetricsMd,
      remoteBranchExists,
    });
    if (decision.action === "skip") return 0;

    // 4. Gate the rendered surface BEFORE publishing — never PR a red doc.
    const freshness = run(
      process.execPath,
      [
        "scripts/check-metric-freshness.mjs",
        "--input",
        renderedPath,
        "--expected-from-success-metrics",
      ],
      { cwd: root, allowFail: true },
    );
    if (freshness.status !== 0) {
      process.stderr.write(
        `daily-metrics-regen: rendered ${METRICS_RELATIVE} fails check-metric-freshness — aborting without a PR\n${freshness.stdout}${freshness.stderr}`,
      );
      return 1;
    }
    const freshnessOutput = `${freshness.stdout}${freshness.stderr}`;

    // 5. Plumbing commit on top of origin/main containing ONLY the rendered
    //    file. GIT_INDEX_FILE isolation keeps the shared index untouched.
    const blob = run("git", ["hash-object", "-w", renderedPath], { cwd: root }).stdout.trim();
    const indexFile = join(scratch, "index");
    /** @type {Record<string, string>} */
    const idxEnv = { GIT_INDEX_FILE: indexFile };
    run("git", ["read-tree", mainSha], { cwd: root, env: idxEnv });
    run("git", ["update-index", "--add", "--cacheinfo", `100644,${blob},${METRICS_RELATIVE}`], {
      cwd: root,
      env: idxEnv,
    });
    const tree = run("git", ["write-tree"], { cwd: root, env: idxEnv }).stdout.trim();
    const commit = run("git", ["commit-tree", tree, "-p", mainSha, "-m", decision.commitMessage], {
      cwd: root,
    }).stdout.trim();

    // 6. Publish: push the commit sha to the dated branch, then open the PR.
    run("git", ["push", "origin", `${commit}:refs/heads/${decision.branch}`], { cwd: root });

    const bodyPath = join(scratch, "pr-body.md");
    writeFileSync(bodyPath, buildPrBody({ dateUtc: today, freshnessOutput }));
    const title = `chore(metrics): daily regen ${today} — keep metric-freshness green`;
    const pr = run(
      "gh",
      [
        "pr",
        "create",
        "--base",
        "main",
        "--head",
        decision.branch,
        "--title",
        title,
        "--body-file",
        bodyPath,
      ],
      { cwd: root },
    );
    process.stdout.write(`daily-metrics-regen: published ${decision.branch}\n${pr.stdout}`);
    return 0;
  } finally {
    rmSync(scratch, { recursive: true, force: true });
  }
}

const invokedDirectly =
  import.meta.url === `file://${process.argv[1]}` ||
  process.argv[1]?.endsWith("daily-metrics-regen.mjs");
if (invokedDirectly) {
  process.exit(cliMain(process.cwd()));
}
